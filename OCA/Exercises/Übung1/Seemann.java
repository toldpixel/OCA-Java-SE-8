class Seemann {
    private String name;
    private int gehalt;

    public void setName(String name) {
        this.name = name;
    } 

    public String getName() {
        return name;
    }

    public void setGehalt(int euro) {
        gehalt = euro;
    }

    public int getGehalt() {
        return gehalt;
    }

}